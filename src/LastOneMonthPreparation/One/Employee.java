package LastOneMonthPreparation.One;

public class Employee {

    private Integer id;
    private String name;
    
    public Employee(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Employee other = (Employee) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        return true;
    }
    
    
}

class EmployeeMain{

    public static void main(String[] args) {
        
        Employee employee = new Employee(1,"teja");
        Employee employeeOne = new Employee(2,"teja");

        System.out.println(employee.equals(employeeOne));

    }
}
